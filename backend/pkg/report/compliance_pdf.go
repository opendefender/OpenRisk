// A4 portrait geometry (mm). Usable width = 210 - 2*margin.
const (
	pageMarginLeft   = 15.0
	pageMarginRight  = 15.0
	pageMarginTop    = 15.0
	pageMarginBottom = 18.0
	usableWidth      = 210.0 - pageMarginLeft - pageMarginRight // 180
	pageBottomLimit  = 297.0 - pageMarginBottom                  // where a row must break
)

// Detailed-table column widths (sum must equal usableWidth = 180).
const (
	colRef    = 22.0
	colName   = 72.0
	colStatus = 30.0
	colEvid   = 18.0
	colSource = 38.0
)

type rgb struct{ r, g, b int }

var (
	brandDark   = rgb{15, 23, 42}    // slate-900 header band
	brandAccent = rgb{37, 99, 235}   // blue-600
	textDark    = rgb{30, 41, 59}    // slate-800
	textMuted   = rgb{100, 116, 139} // slate-500
	hairline    = rgb{203, 213, 225} // slate-300 borders
	zebra       = rgb{243, 246, 250} // very light row tint
	barTrack    = rgb{226, 232, 240} // slate-200

	green = rgb{22, 163, 74}
	amber = rgb{217, 119, 6}
	red   = rgb{220, 38, 38}
	gray  = rgb{100, 116, 139}
)

// statusColor and statusTint return the accent and light-background colors for a status.
func statusColor(s ControlStatus) rgb {
	switch s {
	case StatusImplemented:
		return green
	case StatusInProgress:
		return amber
	case StatusNotImplemented:
		return red
	default:
		return gray
	}
}

func tint(c rgb) rgb {
	// Blend ~12% of the accent color over white for a subtle cell background.
	mix := func(v int) int { return 255 - (255-v)*12/100 }
	return rgb{mix(c.r), mix(c.g), mix(c.b)}
}

